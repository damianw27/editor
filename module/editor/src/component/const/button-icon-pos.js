/***
 * Enum which holds the possible values for icon positioning in button.
 * There are two possible positions that button support.
 * For now its only LEFT and RIGHT.
 *
 * @readonly
 * @property {string} LEFT value that holds the value representing the left position
 * @property {string} RIGHT value that holds the value representing the right position
 * @enum {string}
 */
export const ButtonIconPos = {
  LEFT: 'LEFT',
  RIGHT: 'RIGHT',
};
